a = 321
b = 123

#对示例的代码进行了修改
print("a = {}, b = {}".format(a, b))
print("a + b = {}".format(a + b))
print("a - b = {}".format(a - b))
print("a * b = {}".format(a * b))
print("a / b = {}".format(a / b))
print("a // b = {}".format(a // b))
print("a % b = {}".format(a % b))
print("a ** b = {}".format(a ** b))
